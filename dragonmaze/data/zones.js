// Hand-authored zones adapted from the 5e/EverQuest adventure writeups in
// dungeons/ (Ruins of Guk; The Lost Temple of Cazic-Thule). Pure data.
//
// Map legend: '#' wall · '.' floor · 'S' start
//   'E' surface door (banks & ends the delve) — sits ON a border wall
//   '1'-'9' region doors — sub.doors maps each digit to another subregion id.
//     Doors sit ON border walls (offset into the wall, not floor tiles). You
//     walk *into* a door to travel; you arrive at the paired door on the far
//     region's matching edge (persistent placement), and the run continues —
//     gold, HP, and spent spells all carry over.
// Every map is rectangular; every floor tile (and every door's inner
// neighbour) must be reachable from S (checked by the test suite).
//
// theme: 'grass' | 'sand' | 'swamp' | 'dark' — picks the tileset.
// table: weighted wandering-monster picks — the pool a placed monster rolls
//   from. boss/miniboss: the fixed pack a placed boss/mini spawns (name +
//   preferred item drops).
// difficulty: plays the role of procedural depth for loot and exit bonus.
// WHERE monsters, loot, bosses, and decor sit on each map is not stored here —
// those placements live in data/placements.js, hand-placed with the tile editor
// and keyed by subregion id. This file holds geometry + the identity tables.

