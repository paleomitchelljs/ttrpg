// Monster roster — pure data, no logic. Adding a monster here (and nothing
// else) is enough to put it in the encounter pool once its minDepth is
// reached; maxDepth retires easy monsters from deep labyrinths.
// Stats follow plan §6; abilities are modifiers in roughly −4..+4.
// packMax = most that can appear in one encounter.
// morale = bonus on courage checks; null = fearless (undead/constructs never flee).
// anim = 2-frame strips under assets/sprites/ (idle + attack); monsters
// without one render as their emoji.
// faction: who they answer to, for reputation ('froglok', 'undead', ...).
// parley: 'willing' | 'wary' | 'never' — whether talk is possible at all.
// Mindless dead, constructs, oozes, and beasts simply attack.
// resist/vulnerable = damage types ('physical' | 'fire') taken at half/double.
// ability = keyword the combat engine implements:
//   'regenerate' — heals 2 HP at the start of its turn
//   'relentless' — the first killing blow leaves it at 1 HP instead
//   'lifedrain'  — heals half the damage its attacks deal
